import java.util.List;

  /*
    listShopping = {name:.., listShoppingCategories:[listShoppingCategory1, listShoppingCategory2, ..., listShoppingCategoryN]};
    listShoppingCategory = {id:0, name:'Autre', numRank:5 , ingredients:[{qty:50, unit:"g", food:"ski", rayonId:0}]}
 */

public class ListShopping {
    private String name;
    private List<ListShoppingCategory> listShoppingCategories;

    public ListShopping(){

    }

    public ListShopping(String name, List<ListShoppingCategory> listShoppingCategories) {
        this.name = name;
        this.listShoppingCategories = listShoppingCategories;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public List<ListShoppingCategory> getListShoppingCategories() {
        return listShoppingCategories;
    }

    public void setListShoppingCategories(List<ListShoppingCategory> listShoppingCategories) {
        this.listShoppingCategories = listShoppingCategories;
    }
}
